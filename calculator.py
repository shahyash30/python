num1 = int(input("Enter first number :"))
num2 = int(input("Enter second number :"))

add = num1 + num2
print("the sum of numbers", num1, "+", num2, "is :", add)

sub = num1 - num2
print("the difference of numbers", num1, "-", num2, "is :", sub)

mul = num1 * num2
print("the multiplication of numbers", num1, "x", num2, "is :", mul)

div = num1 / num2
print("the division of numbers", num1, "/", num2, "is :", div)

